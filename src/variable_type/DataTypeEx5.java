package variable_type;

public class DataTypeEx5 {
	
	public static void main(String[] args) {
			
		boolean stop = false; // 1bit , false-> 시작합니다 , true-> 중지합니다
			
		//조건문 프로그램의 논리를 만들때 if 가정 제어문 참과 거짓을 보관
		if(stop) {
			System.out.println("중지합니다.");
		} else {
			System.out.println("시작합니다.");
		} // if-else . 조건문
			
	} // main
	
} // end class

